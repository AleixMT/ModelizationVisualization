package cat.urv.deim.gc.laboratoris;

public class ViewPort {
        private int x;
        private int y;
        private int width;
        private int height;

        public ViewPort(int x, int y, int width, int height) {
            this.x = x;
            this.y = y;
            this.width = width;
            this.height = height;
        }
}
